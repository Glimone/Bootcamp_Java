public class Unary {
    
    public static void main(String[] args) {
        
        /*The unary values:
        + - positive
        - - Negative
        ++ - add +1
        -- - subtract -1
        ! - Recuse a value boolean.
        */

        int number = 5;
        System.out.println(- number); //The '-' did the 5 negative

        number = + number;
        System.out.println(number); //New, we make the exacly, but with '+'.

    }

}
